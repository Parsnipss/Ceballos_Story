"""
Math Functions
"""

#Random Variables
a = 3
b = -6
c = 4

#Addition/Subtraction
print (a + b)
print (a - b)

#Multiplication/Division
print (a * c)
print (b / c)

#Exponents
print (a**b)
print (b**a)

#Square Root/Cube Root
print (a**(1/2))
print (a**.5)
print (b**(1/3))
print (c**(1/4))

#Modulus (%) -- Only returns the remainder AFTER division
print (c%a)
print (c%4)